public class UnaryOp {
    public static void main(String[] args) {
        //Following is an Example of Unary Operator
        int a = 2;
        System.out.println("a++  is " + (a++));//a=2(3)
        System.out.println("++a  is " + (++a));//a=4
        System.out.println("a-- is " + (a--));//a=4(3)
        System.out.println("--a  is " + (--a));//a=2
    }
}
