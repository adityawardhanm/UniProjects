public class BitwiseOp {
    public static void main(String args[]){
        //Following is an Example of Bitwise Operator
        int a=2, b=3, c=4;
        System.out.println("a<b^a<c is " + (a<b^a<c));
        System.out.println("a<b|a<c is " + (a<b|a<c));
        System.out.println("a<b&a<c is " + (a<b&a<c));
    }
}
