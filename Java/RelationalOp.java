public class RelationalOp {
        public static void main(String[] args) {
            int a = 2, b = 3;
            System.out.println("a == b is " + (a == b));
            System.out.println("a != b is " + (a != b));
            System.out.println("a >  b is " + (a > b));
            System.out.println("a >= b is " + (a >= b));
            System.out.println("a <  b is " + (a < b));
            System.out.println("a <= b is " + (a <= b));
        }
}
